local M = {}

function M.cb()
    -- 显示失败旁白，描述修士在灵草谷中遭遇挫折
    LEVEL_ShowDialog("", "灵草谷中妖气冲天，修士道行尚浅，终究难敌千年妖兽...", "sad", 0.8)
    
    -- 体现修仙路上的挫折感
    LEVEL_ShowDialog("", "千年灵草近在咫尺，却如镜花水月般遥不可及。修仙路上，挫折本是常事。", "neutral", 0.8)
    
    -- 符合仙侠修炼永不放弃的精神
    LEVEL_ShowDialog("", "天道酬勤，修仙之路本就充满磨难。今日之败，他日必成修行路上的垫脚石！", "neutral", 0.8)
    
    -- 鼓励性的失败对话
    LEVEL_ShowDialog("", "道心不灭，仙缘不断。待修为精进，再入灵草谷时，必将斩妖取草，证得大道！", "neutral", 0.8)
    
    -- 提示玩家可以重新挑战
    LEVEL_ShowDialog("", "修士暂时退出灵草谷，闭关苦修，待实力大增之日，再来挑战千年蛟龙！", "neutral", 0.8)
    
    -- 最终鼓励
    LEVEL_ShowDialog("", "失败乃成功之母，今日之耻，来日必雪！仙途漫漫，唯有不屈不挠方能登临绝顶！", "neutral", 0.8)
end

return M